package powerlessri.harmonics.gui;

public final class RenderingContext {

    int mouseX;
    int mouseY;
    float particleTicks;

    RenderingContext() {
    }

    public int getMouseX() {
        return mouseX;
    }

    public int getMouseY() {
        return mouseY;
    }

    public float getParticleTicks() {
        return particleTicks;
    }
}
